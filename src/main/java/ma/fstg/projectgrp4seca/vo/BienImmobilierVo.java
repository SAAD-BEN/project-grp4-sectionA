package ma.fstg.projectgrp4seca.vo;

public class BienImmobilierVo {
    private String titreFoncier;
    private String localisation;
    private String minSurface;
    private String maxSurface;

    public String getTitreFoncier() {
        return titreFoncier;
    }

    public void setTitreFoncier(String titreFoncier) {
        this.titreFoncier = titreFoncier;
    }

    public String getLocalisation() {
        return localisation;
    }

    public void setLocalisation(String localisation) {
        this.localisation = localisation;
    }

    public String getMinSurface() {
        return minSurface;
    }

    public void setMinSurface(String minSurface) {
        this.minSurface = minSurface;
    }

    public String getMaxSurface() {
        return maxSurface;
    }

    public void setMaxSurface(String maxSurface) {
        this.maxSurface = maxSurface;
    }

}
